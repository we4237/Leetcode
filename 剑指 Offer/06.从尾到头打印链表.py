# Definition for singly-linked list.
import collections
from typing import List


class ListNode:
    def __init__(self, x):
        self.val = x
        self.next = None

class Solution:
    def reversePrint(self, head: ListNode) -> List[int]:
        stack = []
        while head:
            stack.append(head.val)
            head = head.next
        return stack[::-1]

head = [1,3,2]
solution = Solution()
print(solution.reversePrint(head))